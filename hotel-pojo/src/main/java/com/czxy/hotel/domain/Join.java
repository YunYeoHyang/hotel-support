package com.czxy.hotel.domain;


public class Join {

  private Integer joinId;
  private Integer uid;
  private Integer status;
  private Integer teamNum;
  private Integer intermediaryNum;


  public Integer getJoinId() {
    return joinId;
  }

  public void setJoinId(Integer joinId) {
    this.joinId = joinId;
  }


  public Integer getUid() {
    return uid;
  }

  public void setUid(Integer uid) {
    this.uid = uid;
  }


  public Integer getStatus() {
    return status;
  }

  public void setStatus(Integer status) {
    this.status = status;
  }


  public Integer getTeamNum() {
    return teamNum;
  }

  public void setTeamNum(Integer teamNum) {
    this.teamNum = teamNum;
  }


  public Integer getIntermediaryNum() {
    return intermediaryNum;
  }

  public void setIntermediaryNum(Integer intermediaryNum) {
    this.intermediaryNum = intermediaryNum;
  }

}
